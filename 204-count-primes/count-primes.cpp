class Solution {
public:
    int Solve(int &count, int n) {
        vector<bool> isPrime(n, true);
        if (n <= 2) return 0;

        isPrime[0] = false;
        isPrime[1] = false;
        for (int i = 2; i * i < n; i++) {
            if (isPrime[i]) {

                for (int j = i * i; j < n; j += i) {
                    isPrime[j] = false;
                }
            }
        }

        for (int i = 2; i < n; i++) {
            if (isPrime[i]) count++;
        }


        return count;
    }
    int countPrimes(int n) {
        int count = 0;
        return Solve(count, n);
    }
};















































































































































































































































































































